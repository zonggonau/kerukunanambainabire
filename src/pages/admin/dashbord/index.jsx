export async function getServerSideProps(ctx) {
  const cookie = nookies.get(ctx);
  const user = {
    id: cookie.id,
    name: cookie.name,
    email: cookie.email,
    admin: cookie.admin,
    token: cookie.token,
  };

  if (!cookie.token) {
    return {
      redirect: {
        destination: "/",
      },
    };
  }

  if (cookie.admin !== "admin") {
    return {
      redirect: {
        destination: "/user/profile/" + user.id,
      },
    };
  }

  const qs = require("qs");
  const query = qs.stringify(
    {
      // filters: {
      //   jabatan_kerukunan: {
      //     $eq: "Ketua Keret",
      //   },
      // },
      populate: "*",
    },

    {
      encodeValuesOnly: true, // prettify URL
    }
  );

  const res = await fetch(
    process.env.NEXT_PUBLIC_HOST + "/api/users?" + query,
    {
      headers: {
        Authorization: `Bearer ${process.env.NEXT_PUBLIC_TOKEN}`,
      },
    }
  );
  const data = await res.json();
  console.log(data);

  return {
    props: {
      user,
      data,
    },
  };
}
